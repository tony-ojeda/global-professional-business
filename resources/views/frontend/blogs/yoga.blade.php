                            <p class="fs-5 mb-4 text-justify">Lima, Peru, is a tourist&#39;s paradise, known for its rustic red sandstone and eclectic art scene.
However, the mother-daughter duo of Karina and Gianella de Lurin were not only drawn to the
stunning views but also yoga. They participated in a five-day seminar in which they practiced
Jnankanda Yoga at the Lima Cultural Center for ten hours a day. At the end of this experience,
they each wrote down their aspirations. Both Karina and Gianella, without knowing it until that
moment, wrote the same aspiration: to open a studio.</p>
                            <p class="fs-5 mb-4 text-justify">From there, <b>&quot;Yoga Studio Kari &amp; Giane&quot;</b> was born. On the eve of the sixth anniversary of the
studio&#39;s opening, we spoke with Karina and Gianella about the goal of making yoga more
accessible to diverse communities and how it became a reality.</p>
                        <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga2.png') }}" alt="..." /></figure>

                            <ul>
                                <li><b>How did you start yoga?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> When I was 14 years old, I liked to walk and ride my bike, but I had an accident with my
bike, and I was disabled because of several injuries and the doctor said I couldn&#39;t even run or
jump anymore. The only thing that helped me was yoga, something that helped me physically.</p>
                                <p class="fs-5 mb-4 text-justify">Finally, I understood that it was not only physical but also mental. The moment I did yoga, it
rebalanced me, and I had a connection to practicing yoga, it balanced my mental state, plus the
benefit of feeling fit. And my mother saw that I aspired to do yoga. And she started researching
yoga.</p>

                                <li><b>What motivated you to open your studio?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> Out of a hundred people in this seminar in Lurin, my mother and I were one of the four people who had a dream and we asked ourselves: &quot;Why are there only four of us? At the end of the seminar, we had to write an aspiration or dream, where we wanted to go. I wrote that someday I wanted to open the yoga studio to serve a larger number of people, especially in a diverse community. My mother also wrote the same thing in her notebook: I would like to help
my daughter start a yoga studio. At that time, we had no idea.</p>
                                <p class="fs-5 mb-4 text-justify">That&#39;s how it started. I simply wanted to open yoga to a larger number of people. It has been
too insular a community; now it is changing, although historically in Peru it has not been
something very open to the diversity of groups.</p>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga3.png') }}" alt="..." /></figure>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga4.png') }}" alt="..." /></figure>

                                <li><b>How did you find the studio?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> There was a studio very close to my house, so I thought, why don&#39;t I investigate if I can
teach there. So, I contacted them.</p>
                                <p class="fs-5 mb-4 text-justify">My mother asked me how I could undertake that Yoga business, at that time we both joined,
and I realized that she was thinking about the Yoga studio, so we went ahead to open a Yoga
studio. It was in July 2014, where we accidentally came up with all this. On Peru&#39;s Independence
Day, we opened <b>Yoga Studio Kari &amp; Giane.</b></p>

                                <li><b>What distinguishes your studio?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Gianella:</b> I think one of the unique things we have is the love of Karina and me, who are mother
and daughter, for yoga, not only for the practice of yoga in general but also for a transformative
yoga practice. Because of our professional backgrounds - Karina is a psychologist and I am a
customer service worker - we looked at the ability to change the fabric of being human, the way
we see the world more cohesively.</p>
                                <p class="fs-5 mb-4 text-justify">When we started, we didn&#39;t feel that we both approached it as if we were inviting people into
our home. Kari &amp; Giane is our yoga studio and we had to clean our studio the same way we
clean our house, although we do it daily. Another thing we are guided by is that all our teachers
are trained in welcoming attendees into our studio. The teachers as well as the front desk staff
welcome everyone. The other aspect is that you are never the first one to leave the room. After
a class, you don&#39;t rush out. You wait until all the students have left and then the teacher can
leave.</p>
                                <p class="fs-5 mb-4 text-justify">Another unique aspect of our studio is that we believe yoga is meant for everyone, every
B-O-D-Y. This is our essence. This is our brand. Whoever can breathe, can do yoga. When we
opened, I was 45 years old, and my daughter was younger. So, we were two different
generations. Having an older person as the owner teaching the class, that&#39;s where the diversity
was. Also, we made them feel comfortable when they came to our studio, although it also
made others feel comfortable because we were visited by many tourists and some clients have
told us that when they come to the studio, they feel different.</p>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga5.png') }}" alt="..." /></figure>

                                <li><b>How have you made yoga accessible to different communities?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Gianella:</b> One thing we do is engage the public through word of mouth. We have yin yoga-style
classes, which are mostly on the mat. They are longer and have a more therapeutic as well as
gentle nature. But we also have power flow yoga classes, which are aerobic and are meant to
work up a sweat. We try to have classes for everyone. Often young people come to the power
classes, and I&#39;m in the front and they talk with me. Then the young people always bring
someone with them, such as their mother or a friend for the yoga class. The mother arrives and
checks that everything is going well so she comes over with Karina while she is teaching the
class.</p>
                                <p class="fs-5 mb-4 text-justify">We are in a diverse community; we understand why yoga is attracting so many people. But
yoga costs a lot. That&#39;s why we have classes for the community for $10 and twice a week, currently, we do them virtually. We have a variety of packages and prices, in addition to the
traditional unlimited membership. If we combine that with some of the $10 classes, people
who couldn&#39;t come before - maybe seniors and people on fixed incomes, students - can still
come to the <b>Yoga Studio Kari &amp; Giane.</b></p>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga6.png') }}" alt="..." /></figure>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga7.png') }}" alt="..." /></figure>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga8.png') }}" alt="..." /></figure>

                                <li><b>How has the mother-daughter business experience been?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> It has been wonderful to have my mother as a partner. As a co-owner of a company,
good partners are very important, partners with whom you can trust, communicate, and have
the same level of respect, even if you don&#39;t agree on the same thing. In the company we don&#39;t
have a mother-daughter bond, we are business partners. We divide responsibilities or
delegations. I take care of the operation while she takes care of the administration and
finances. I am very well thankful because owning a business is not easy, owning a small
business is extremely difficult, so owning a minority business has an additional twist. I couldn&#39;t
have thought of a better partner.</p>
                                <p class="fs-5 mb-4 text-justify">I don&#39;t think it occurred to any of us that we would be where we are today, unleashing a small
business that we are fully invested in. But then again, we&#39;re still here, in the middle of a
pandemic.</p>
                                <p class="fs-5 mb-4 text-justify"><b>Gianella:</b> My daughter and I have always been best friends. We understand perfectly what
&quot;divide or conquer&quot; is. She has her strengths and knowledge, I have mine. We advise each other
to keep the company moving forward, although we can give each other advice...</p>

                                <li><b>How did COVID-19 affect your company?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> The impact of COVID-19 was huge, even for places that rely on in-person services. This
yoga business is based on the group fitness model, which means the more people in the room,
the better the profit margins. At COVID, it is impossible to offer 40 people in our room. We
can&#39;t even offer 10 people in the room, because they feel uncomfortable. This business model
does not work.</p>
                                <p class="fs-5 mb-4 text-justify">The change in operations has helped slow the leakage, although it hasn&#39;t stopped it either. All
the small businesses are trying to find new livelihoods. There has been an increase in the
number of business yoga classes we teach, which has helped.</p>
                                <p class="fs-5 mb-4 text-justify"><b>Gianella:</b> The impact of the pandemic on our business has been great. Profits are down
considerably. Obviously, for most of the year, we haven&#39;t been able to teach in-person classes
at the studio, but our community has been great. We retained at least 80% of the membership,
and we are enormously grateful for that.</p>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga9.png') }}" alt="..." /></figure>
                                <figure class="mb-4 text-center"><img class="img-fluid rounded" src="{{ asset('storage/blog/yoga10.png') }}" alt="..." /></figure>

                                <li><b>What advice would you give to aspiring entrepreneurs or other business
owners?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> The first thing is to make sure you have all the resources possible to start your own
business. It is more difficult when you have investors because they will have their demands,
requirements to meet, and time frames to get their money back.</p>
                                <p class="fs-5 mb-4 text-justify">Another recommendation is to have great confidence in what you are trying to do and be
strong. Have a very clear vision and stick to it only and exclusively. When you are clear about
that vision, make sure you know how to transmit it to others, because a successful business can
only be born in your heart. Your staff or your team should oversee carrying it out.</p>
                                <p class="fs-5 mb-4 text-justify">Make sure you know what you are a &quot;yes&quot; to make your business dreams come true as well as
what you are a &quot;no&quot; to. You can&#39;t say &quot;yes&quot; to everything. People, your customers, may try to
influence you, and you must make sure that what your customers are asking you to do is
something you really want to do and can fully invest in. As a lesson learned and what made it
difficult for us as owners, you have to be very aware of the judgments and perceptions that a
majority culture may have about your business and how you are going to mitigate those
judgments.</p>


                                <li><b>What are you most interested in?</b></li>
                                <p class="fs-5 mb-4 text-justify"><b>Karina:</b> We have a firm resolve to overcome this pandemic. When we get through it completely
or get to the other side, it won&#39;t be the same. Although we hope to continue to provide that
same vision where everyone is welcome, feels supported, where if there is nothing else in your
life, there is a place that is like Kari &amp; Giane, where there are kind, and who are here to ensure
your health as well as your well-being. All that is necessary to do that.</p>
                                <p class="fs-5 mb-4 text-justify">Neither my mother nor I pay us. we make sure that we pay other people. We always wanted to
make sure we could help others and while Kari and Giane can pay the rent, their taxes and
insurance, and everything else goes back into the community.</p>
                                <p class="fs-5 mb-4 text-justify">This interview was edited or condensed. Photos of <b>Yoga Studio Kari &amp; Giane</b>.</p>

